namespace Cclark61\RPi;

class Core
{
    //=========================================================================
    // Class Members
    //=========================================================================

    //=========================================================================
    //=========================================================================
    // Script Start Header
    //=========================================================================
    //=========================================================================
    public static function ScriptStart($msg)
    {
        print "*********************************************************************\n";
        print "*** {$msg}";
        print "\n*********************************************************************\n";
    }

    //=========================================================================
    //=========================================================================
    // Script Message
    //=========================================================================
    //=========================================================================
    public static function ScriptMessage($msg)
    {
        print "\n*-> {$msg}\n";
    }

    //=========================================================================
    //=========================================================================
    // Exit with Error
    //=========================================================================
    //=========================================================================
    public static function ExitWithError($msg)
    {
        print "\n[!!] Error: {$msg}\n";
        exit;
    }

    //=========================================================================
    //=========================================================================
    // Script Ending Message
    //=========================================================================
    //=========================================================================
    public static function ScriptEnd($msg='')
    {
        if ($msg == '') {
            $msg = 'Done.';
        }
        print "\n\n{$msg}\n";
    }

    //=========================================================================
    //=========================================================================
    // Confirm Contiune
    //=========================================================================
    //=========================================================================
    public static function ConfirmContinue($msg='')
    {
        //---------------------------------------------------------------------
        // No message? Use Default.
        //---------------------------------------------------------------------
        if (!$msg) {
            $msg = 'Would you like to continue?';
        }

        //---------------------------------------------------------------------
        // Confirm Proceed
        //---------------------------------------------------------------------
        print "\n\n{$msg} To proceed, type 'YES': ";
        while ($stdin = fgets(STDIN)) {
            $stdin = trim($stdin);
            if ($stdin) { break; }
        }
        if ($stdin != 'YES') {
            print "\nExiting.\n";
            exit;
        }
    }

    //==========================================================================
    //==========================================================================
    // Ctrl-C Method
    //==========================================================================
    //==========================================================================
    public static function CtrlC()
    {
        GPIO::TurnOffAll();
        exit;
    }

}
